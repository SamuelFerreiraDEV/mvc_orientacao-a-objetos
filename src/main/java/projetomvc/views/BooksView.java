    private void displayBooks(boolean updateActionResultArea, boolean updateBooksList) {
        List<Book> books = this.searchBooks();
        if (updateActionResultArea) {
            setBooksResult(books);
        }
        if (updateBooksList) {
            this.updateBooksList(books);
        }
    }

    private List<Book> searchBooks() {
        HashMap<String, String> params = this.buildParams();
        List<Book> books = this.bookController.index(params);

        if (books != null && !books.isEmpty()) {
            return books;
        } else {
            JOptionPane.showMessageDialog(this, "Livro não encontrado.", "Busca", JOptionPane.INFORMATION_MESSAGE);
            return null;
        }
    }

    private HashMap<String, String> buildParams() {
        HashMap<String, String> params = new HashMap<>();
        int authorId = this.getAuthorIdByName(this.comboBoxAuthors.getSelectedItem().toString());
        
        params.put("title", this.fieldTitle.getText());
        params.put("authorId", String.valueOf(authorId));
        params.put("year", String.valueOf(this.fieldPublishedYear.getText()));

        return params;
    }

    private void setBooksResult(List<Book> books) {
        if (books != null && !books.isEmpty()) {
            this.clearTextFields();
            this.actionResultArea.setText("Livros encontrados:\n");
            for (Book book : books) {
                this.actionResultArea.append("Título: " + book.getTitle() + ", Autor: " + authorController.show(book.getAuthorId()).getName() + ", Ano: " + book.getPublishedYear() + "\n");
            }
        }
    }

    private void updateBooksList(List<Book> books) {
        if (books != null && !books.isEmpty()) {
            this.booksList.setListData(books.stream().map(book -> book.getId() + ": " + book.getTitle()).toArray(String[]::new));
        }
    }

    private void booksListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_booksListValueChanged
        String value = this.booksList.getSelectedValue();
        if (value != null && !value.trim().isEmpty()) {
            this.selectedBookId = Integer.parseInt(value.split(":")[0]);
            this.buttonEdit.setEnabled(true);
            this.buttonDelete.setEnabled(true);
        } else {
            this.buttonEdit.setEnabled(false);
            this.buttonDelete.setEnabled(false);
            this.selectedBookId = null;
        }
    }//GEN-LAST:event_booksListValueChanged

    private Book buildBookFromInputs() {
        Book book = new Book();
        int authorId = this.getAuthorIdByName(this.comboBoxAuthors.getSelectedItem().toString());

        book.setTitle(this.fieldTitle.getText());
        book.setAuthorId(authorId);
        book.setPublishedYear(Integer.parseInt(this.fieldPublishedYear.getText()));

        return book;
    }

    private void loadAuthors() {
        this.comboBoxAuthors.removeAllItems();
        this.comboBoxAuthors.addItem("Selecione um autor");
        List<Author> authors = authorController.index(new HashMap<>());

        for(Author author : authors) {
            this.comboBoxAuthors.addItem(author.getName());
        }
    }
