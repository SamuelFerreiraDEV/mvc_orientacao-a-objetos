    /* Refactor to dropdown */
    public int getAuthorIdByName(String name) {
        HashMap<String, String> params = new HashMap<>();
        params.put("name", name);

        List<Author> author = this.authorController.index(params);
        if (author != null && !author.isEmpty()) {
            return author.get(0).getId();
        }
        return -1;
    }

    private void clearTextFields() {
        for(JTextField field : textFields) {
            field.setText("");
        }
    }

    private void displayActionResultText(String action, boolean success, Book book) {
        String result = "";
        switch (action) {
            case "save":
                result = success ? "Livro salvo: " + book.getTitle() : "Erro ao salvar livro.";
                break;
            case "update":
                result = success ? "Livro editado: " + book.getTitle() : "Erro ao editar livro.";
                break;
            case "delete":
                result = success ? "Livro removido: " + book.getTitle() : "Erro ao remover livro.";
                break;
        }
        this.actionResultArea.setText(result);
        
    }

    private boolean anyFieldEmpty() {
        for(JTextField f : textFields) {
            if (f == null || f.getText() == null || f.getText().trim().isEmpty()) {
                return true;
            }
        }
        return false;
    }

    private boolean isYearValid() {
        String year = this.fieldPublishedYear.getText();
        try {
            int y = Integer.parseInt(year);
            return y > 0;
        } catch (NumberFormatException e) {
            return false;
        }
    }

    private boolean fieldsValid() {
        if (this.anyFieldEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Validação", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if (!this.isYearValid()) {
            JOptionPane.showMessageDialog(this, "Ano inválido.", "Validação", JOptionPane.WARNING_MESSAGE);
            return false;
        }

        return true;
    }


